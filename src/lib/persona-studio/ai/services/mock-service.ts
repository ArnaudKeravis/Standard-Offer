/**
 * Deterministic, clearly-labelled MOCK AI service for Phase 2.
 *
 * It does NOT call any model. It produces structured, schema-valid output from
 * a fixed catalogue of behavioural patterns so the create wizard is fully
 * demonstrable offline. Crucially, it respects every grounding rule:
 *  - nothing it emits is tagged EVIDENCE (drafts are ASSUMPTION / TO_VALIDATE),
 *  - quotes are DRAFTED_HYPOTHESIS,
 *  - generated confidence is LOW,
 *  - the `meta.disclaimer` makes clear the content is machine-drafted, not
 *    research.
 *
 * Phase 3/5 swaps this for an OpenAI-backed implementation behind the same
 * {@link PersonaAiService} interface.
 */


function meta(): GenerationMeta {
  return {
    service: SERVICE_NAME,
    deterministic: true,
    disclaimer: DISCLAIMER,
    generatedAt: new Date().toISOString(),
  };
}

function excerptsFrom(text: string): string[] {
  const trimmed = text.trim();
  if (!trimmed) return [];
  return [trimmed.length > 160 ? `${trimmed.slice(0, 157)}…` : trimmed];
}

export class MockPersonaAiService implements PersonaAiService {
  async analyseResearchSources(
    input: AnalyseSourcesInput,
  ): Promise<SourceAnalysisResult> {
    const sourceIds = input.sources.map((s) => s.id);
    const supporting = input.sources.flatMap((s) =>
      excerptsFrom(s.extractedText),
    );

    const insights: SourceInsight[] = PATTERN_CATALOGUE.map((p, i) => ({
      id: `insight-${input.project.id}-${i}`,
      theme: p.theme,
      summary: p.summary,
      sourceIds,
      supportingExcerpts: supporting.slice(0, 2),
      frequency: sourceIds.length,
    }));

    return { meta: meta(), insights };
  }

  async generateBehaviouralClusters(
    input: GenerateClustersInput,
  ): Promise<ClusterSet> {
    const insights = input.analysis.insights;
    const count = clamp(
      input.desiredClusterCount ?? Math.min(4, Math.max(2, insights.length)),
      1,
      PATTERN_CATALOGUE.length,
    );

    const share = round2(1 / count);
    const clusters: BehaviouralCluster[] = [];
    for (let i = 0; i < count; i++) {
      const p = PATTERN_CATALOGUE[i % PATTERN_CATALOGUE.length];
      const insight = insights[i % Math.max(1, insights.length)];
      clusters.push({
        id: `cluster-${i}-${slug(p.archetype)}`,
        label: p.theme,
        summary: p.summary,
        suggestedArchetype: p.archetype,
        behaviouralTags: p.tags,
        needSignals: p.needs,
        frustrationSignals: p.frustrations,
        sourceIds: insight?.sourceIds ?? [],
        estimatedShare: share,
        confidence: "LOW",
      });
    }

    return { meta: meta(), clusters };
  }

  async generatePersonaFromCluster(
    input: GeneratePersonaInput,
  ): Promise<Persona> {
    const { cluster, project, template } = input;

    const persona = scaffoldPersona({
      projectId: project.id,
      template,
      family: project.family,
      name: `Draft: ${cluster.label}`,
      archetype: cluster.suggestedArchetype,
      category: cluster.label,
      oneLineEssence: cluster.summary,
      quote: draftedQuote(cluster),
      quoteType: "DRAFTED_HYPOTHESIS",
      confidenceLevel: "LOW",
      confidenceExplanation:
        "Machine-drafted from a behavioural cluster, not from field research. Every statement is an assumption or is pending validation.",
      behaviouralTags: cluster.behaviouralTags,
      sourceIds: cluster.sourceIds,
    });

    // Fill the scaffolded sections with clearly-labelled draft statements.
    const fill = (
      section: PersonaSection,
    ): PersonaSection => {
      const items = draftStatementsFor(section.key, cluster, section.id);
      return items.length > 0 ? { ...section, statements: items } : section;
    };

    return {
      ...persona,
      commonSections: persona.commonSections.map(fill),
      domainSections: persona.domainSections.map(fill),
    };
  }

  async auditPersona(persona: Persona): Promise<PersonaAuditResult> {
    return reviewPersona(persona);
  }
}

const NEED_KEYS = ["needs", "key_expectations", "expectations", "workplace_expectations"];
const FRUSTRATION_KEYS = ["frustrations", "pains"];
const BEHAVIOUR_KEYS = ["behaviours", "context"];
const ESSENCE_KEYS = ["essence"];

function draftStatementsFor(
  key: string,
  cluster: BehaviouralCluster,
  sectionId: string,
): PersonaStatement[] {
  const make = (
    content: string,
    idx: number,
    status: PersonaStatement["evidenceStatus"],
  ): PersonaStatement => ({
    id: `${sectionId}-draft-${idx}`,
    content,
    evidenceStatus: status,
    confidence: "LOW",
    sourceIds: [],
    editable: true,
  });

  if (ESSENCE_KEYS.includes(key)) {
    return [make(cluster.summary, 0, "ASSUMPTION")];
  }
  if (NEED_KEYS.includes(key)) {
    return cluster.needSignals.map((c, i) => make(c, i, "TO_VALIDATE"));
  }
  if (FRUSTRATION_KEYS.includes(key)) {
    return cluster.frustrationSignals.map((c, i) => make(c, i, "TO_VALIDATE"));
  }
  if (BEHAVIOUR_KEYS.includes(key)) {
    return cluster.behaviouralTags.map((c, i) =>
      make(`Tends to be ${c}.`, i, "ASSUMPTION"),
    );
  }
  return [];
}

function draftedQuote(cluster: BehaviouralCluster): string {
  const need = cluster.needSignals[0]?.toLowerCase() ?? "a seamless experience";
  return `What matters most to me is ${need}.`;
}

function slug(input: string): string {
  return input.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "");
}

function clamp(n: number, min: number, max: number): number {
  return Math.min(max, Math.max(min, n));
}

function round2(n: number): number {
  return Math.round(n * 100) / 100;
}
